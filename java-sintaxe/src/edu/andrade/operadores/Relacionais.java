package edu.andrade.operadores;

public class Relacionais {
    public static void main(String[] args) {
        int numero1 = 1;
        int numero2 = 2;

       if (numero1==numero2){
           System.out.println("Verdadeiro");
       }else {
           System.out.println("Falso");
       }
       if (numero1!=numero2){
           System.out.println("Verdadeiro");
       }else
           System.out.println("Falso");
    }
}
